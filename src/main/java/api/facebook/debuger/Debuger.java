    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (album == null) {
            String albumName = JOptionPane.showInputDialog(this, "Ingresa el nombre para un album", "Mensaje", JOptionPane.QUESTION_MESSAGE);
            Optional<UserAlbum> createUserAlbum = Facebook.createUserAlbum(albumName, user.getId(), token.getAccessToken());
            if (createUserAlbum.isPresent()) {
                setAlbum(createUserAlbum.get());
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo crear el album, vuelve a intentarlo", "Mensaje", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        if (Facebook.publishAlbumPhotosFile(foto, tCaption1.getText(), album.getId(), token.getAccessToken())) {
            JOptionPane.showMessageDialog(this, "Se publico correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo publicar, vuelve a intentarlo", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

