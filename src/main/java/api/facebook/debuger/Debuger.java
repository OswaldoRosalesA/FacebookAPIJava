    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        String old = this.code;
        this.code = code;
        firePropertyChange(PROP_CODE, old, code);
        Facebook.userToken(code).ifPresent(this::setToken);

        pContent.setVisible(true);
        bIn.setVisible(false);
        bOut.setVisible(true);
    }

    public UserToken getToken() {
        return token;
    }

    public void setToken(UserToken token) {
        UserToken old = this.token;
        this.token = token;
        firePropertyChange(PROP_TOKEN, old, token);
        Facebook.userInfo(token.getAccessToken()).ifPresent(this::setUser);
    }

    public UserData getUser() {
        return user;
    }

    public void setUser(UserData user) {
        UserData old = this.user;
        this.user = user;
        firePropertyChange(PROP_USER, old, user);
        jLabel3.setText(user.getName());
    }

    public UserAlbum getAlbum() {
        return album;
    }

    public void setAlbum(UserAlbum album) {
        UserAlbum old = this.album;
        this.album = album;
        firePropertyChange(PROP_ALBUM, old, album);
    }

